export const FAMILY_FEUD_QUESTIONS_COUNT = FAMILY_FEUD_QUESTIONS.length;

export function getFamilyFeudQuestionBank() {
  return FAMILY_FEUD_QUESTIONS.map((item) => ({
    question: String(item.question || "").trim(),
    answers: Array.isArray(item.answers)
      ? item.answers
          .map((answer) => ({
            text: String(answer.text || "").trim(),
            points: Number(answer.points || 0),
            revealed: false
          }))
          .filter((answer) => answer.text && answer.points > 0)
      : []
  })).filter((item) => item.question && item.answers.length > 0);
}

export function getFamilyFeudQuestion(index = 0) {
  const bank = getFamilyFeudQuestionBank();

  if (!bank.length) {
    return {
      question: "نص السؤال",
      answers: []
    };
  }

  const safeIndex = normalizeQuestionIndex(index, bank.length);
  return bank[safeIndex];
}

export function normalizeQuestionIndex(index = 0, total = FAMILY_FEUD_QUESTIONS.length) {
  const n = Number(index);

  if (!Number.isFinite(n) || total <= 0) {
    return 0;
  }

  return ((Math.trunc(n) % total) + total) % total;
}
